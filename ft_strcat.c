#include "libft.h"

char	*ft_strcat(char *restrict dest, const char *restrict src)
{
	int i;
	int j;

	j = 0;
	i = ft_strlen(dest);
	while (src[j])
	{
		dest[i] = src[j];
		i++;
		j++;
	}
	dest[i] = '\0';
	return (dest);
}
